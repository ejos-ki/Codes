    private void LOGINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LOGINActionPerformed
        String userID = USERID.getText();
        String password = new String(PASSWORD.getPassword());

        if (userID.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please complete the data.");
        } else {
            if (userID.equals("ADMIN") && password.equals("USERDATABASE")) {
                dispose(); // Close the current login frame

                // Open the DATABASE frame
                java.awt.EventQueue.invokeLater(() -> {
                    new DATABASE().setVisible(true);
                });

            } else {
                if (counterFromFacade == 0) {
                    // Read from FACULTY_ACCOUNT.java file and store IDs and passwords in arrays
                    // Use the code to read and process data from FACULTY_ACCOUNT.java file
                    // Example:
                    String fileName = "FACULTY_ACCOUNT.txt"; // Change this to the correct file name

                    ArrayList<String> ID0 = new ArrayList<>();
                    ArrayList<String> PASS0 = new ArrayList<>();
                    ArrayList<String> LN0 = new ArrayList<>();
                    ArrayList<String> FN0 = new ArrayList<>();
                    ArrayList<String> SUB0 = new ArrayList<>();

                    try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
                        String line;
                        while ((line = br.readLine()) != null) {
                            String[] parts = line.split("\\s+");
                            ID0.add(parts[2]); // Assuming userID is at index 2 in the file
                            PASS0.add(parts[3]); // Assuming password is at index 3 in the file
                            LN0.add(parts[0]); // Assuming last name is at index 0 in the file
                            FN0.add(parts[1]);
                            SUB0.add(parts[4]);
                        }
                    } catch (IOException e) {
                        e.printStackTrace(); // Handle the exception properly in your application
                    }

                    int index = ID0.indexOf(userID);
                    if (index != -1 && PASS0.get(index).equals(password)) {
                        dispose(); // Close the current login frame

                        String lastName = LN0.get(index);
                        String firstName = FN0.get(index);
                        String course = SUB0.get(index);

                        openFacultyFrame(userID, lastName, firstName, course);
                    } else {
                        JOptionPane.showMessageDialog(null, "Invalid Input.");
                        clearUserInput();
                    }
                } else if (counterFromFacade == 1) {
                    // Read from STUDENT_ACCOUNT.java file and store IDs and passwords in arrays
                    String fileName = "STUDENT_ACCOUNT.txt"; // Change this to the correct file name

                    ArrayList<String> ID1 = new ArrayList<>();
                    ArrayList<String> PASS1 = new ArrayList<>();
                    ArrayList<String> LN1 = new ArrayList<>();
                    ArrayList<String> FN1 = new ArrayList<>();

                    try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
                        String line;
                        while ((line = br.readLine()) != null) {
                            String[] parts = line.split("\\s+");
                            ID1.add(parts[2]); // Assuming userID is at index 2 in the file
                            PASS1.add(parts[3]); // Assuming password is at index 3 in the file
                            LN1.add(parts[0]); // Assuming last name is at index 0 in the file
                            FN1.add(parts[1]);
                        }
                    } catch (IOException e) {
                        e.printStackTrace(); // Handle the exception properly in your application
                    }

                    int index = ID1.indexOf(userID);
                    if (index != -1 && PASS1.get(index).equals(password)) {
                        dispose(); // Close the current login frame

                        String lastName = LN1.get(index);
                        String firstName = FN1.get(index);
                        openStudentFrame(userID, lastName, firstName);
                    } else {
                        JOptionPane.showMessageDialog(null, "Invalid Input.");
                        clearUserInput();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Counter value not recognized.");
                }
            }
        }
    }//GEN-LAST:event_LOGINActionPerformed

