    private void clearTextFieldOnClick(javax.swing.JTextField textField) {
        textField.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                if (!textField.getText().isEmpty()) {
                    textField.setText("");
                    textField.removeMouseListener(this); // Remove the listener after clearing
                }
            }
        });
    }
    
